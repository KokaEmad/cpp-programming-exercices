#include <iostream>
using namespace std;

int main(){
int A;
int B;
cout << "Please enter the first Number A?\n";
cin >> A;

cout << "Please enter the first Number B?\n";
cin >> B;

cout << A << " + " << B << " = " << A + B << "\n";
cout << A << " * " << B << " = " << A * B << "\n";
cout << A << " - " << B << " = " << A - B << "\n";
cout << A << " / " << B << " = " << A / B << "\n";
cout << A << " % " << B << " = " << A % B << "\n";
return 0;

}